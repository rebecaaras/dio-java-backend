public class Operadores {
    
    public static void main(String[] args){
        int numero1 = 1;
        int numero2 = 1;

        boolean resultado = numero1 == numero2;

        System.out.println("numero1 é igual ao numero2? " + resultado);

        resultado = numero1 != numero2;

        System.out.println("numero1 é diferente ao numero2? " + resultado);
    }
}
